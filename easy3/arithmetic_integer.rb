# Easy 3 - 2 Arithmetic integer

puts "Enter your first number:"
number_1 = gets.chomp.to_i
puts "Enter your second number:"
number_2 = gets.chomp.to_i

puts "#{number_1} + #{number_2} = #{number_1 + number_2}"
puts "#{number_1} - #{number_2} = #{number_1 - number_2}"
puts "#{number_1} * #{number_2} = #{number_1 * number_2}"
puts "#{number_1} / #{number_2} = #{number_1 / number_2}"
puts "#{number_1} % #{number_2} = #{number_1 % number_2}"
puts "#{number_1} ** #{number_2} = #{number_1 ** number_2}"